#include <stdio.h>
int main(void) {
  //Creat a variable to store an integer value
  int age;
  //Assigne a value to that variable
  age = 29;
  printf("I am %d.\n", age);
  printf("In %d years, I will be %d years old.\n", 8, age+8);
  printf("%d years ago, I was %d years old.\n", 11, age-11);
  return 0;
}
